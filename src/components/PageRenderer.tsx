import { getSectionDefinition } from '@/lib/sections/registry';

export function PageRenderer({ sections, systemSettings = {}, isEditing = false }: { sections: any[], systemSettings?: any, isEditing?: boolean }) {
    if (!sections || sections.length === 0) {
        return null;
    }

    return (
        <div className="flex flex-col w-full">
            {sections.map((section) => {
                const definition = getSectionDefinition(section.type);

                if (!definition) {
                    console.warn(`Unknown section type: ${section.type}`);
                    return null;
                }

                const Component = definition.component;

                return (
                    <Component
                        key={section.id}
                        data={section.data}
                        style={section.style}
                        systemSettings={systemSettings} // Pass systemSettings to all components
                        isEditing={isEditing}
                    />
                );
            })}
        </div>
    );
}
